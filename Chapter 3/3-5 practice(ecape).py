#저는 "카카오코크" 입니다.
print("저는 '카카오코크'입니다.") # 문자열은 ""로 감싸져 구분 됨
print('저는 "카카오코크"입니다.') # 작은 따옴표로 감쌀 수 있으나 해당 구문만 작은따옴표로 쓰면 작업이 효율적이지 못함
print("저는 \"카카오코크\"입니다.") # \를 사용하여 escape 할 수 있음 문장 내 에서 큰 따옴표를 그대로 출력하는 역할
print("저는 \'카카오코크\'입니다.")

