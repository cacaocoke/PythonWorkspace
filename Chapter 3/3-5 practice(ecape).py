# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") # 커서를 앞으로 이동 하면서 한 문자열을 대체함 따라서 PineAple로 출력 됨 

# \b : 백스페이스 (한글자 삭제)
print("Redd\bApple") # Red'd'를 삭제함

# \t : 탭(들여쓰기)

print("Red\tApple") # Tab 기능의 들여쓰기로 작동, Red    Apple로 출력 됨